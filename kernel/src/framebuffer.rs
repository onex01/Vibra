    pub fn print_num(&mut self, mut n: usize) {
        if n == 0 {
            self.put_char('0');
            return;
        }
        // Временный буфер на стеке
        let mut buf = [0u8; 20];
        let mut i = 20usize;
        while n > 0 {
            i -= 1;
            buf[i] = b'0' + (n % 10) as u8;
            n /= 10;
        }
        if let Ok(s) = core::str::from_utf8(&buf[i..]) {
            self.print(s);
        }
    }

